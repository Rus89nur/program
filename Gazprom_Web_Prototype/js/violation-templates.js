const ViolationTemplates = (() => {
  const VIOLATION_TYPES = [
    'Обучение работников в области производственной безопасности',
    'Обеспечение работников СИЗ, применении работниками СИЗ',
    'Работы на высоте',
    'Пожароопасные работы',
    'Газоопасные работы',
    'Земляные работы',
    'Погрузочно-разгрузочные работы,складирование материалов',
    'Эксплуатация инструмента и приспособлений',
    'Эксплуатация машин и механизмов, подъёмных сооружений, подъёмных средств, подъёмных механизмов',
    'Эксплуатация, перевозка, хранение баллонов с сжиженным газом и газовых баллонов',
    'Пожарная безопасность',
    'Электробезопасность',
    'Безопасность дорожного движения, перевозка пассажиров и грузов',
    'Санитарно-бытовое обеспечение',
    'Организация внутреннего контроля за соблюдением требований производственной безопасности на ОРП',
    'Организация работы с происшествиями (несчастными случаями, авариями, инцидентами, пожарами, транспортными происшествиями)',
    'Прочие работы',
  ];

  /** Новые виды из Smart Forms: показываем в разделе «Сопоставить». */
  const MAPPING_SEED_TYPES = [
    'Нарушение требований по безопасному производству работ на высоте',
    'Нарушение требований по безопасному производству огневых работ',
    'Нарушение требований к производству газоопасных работ',
    'Нарушение требований по безопасному производству земляных работ',
    'Нарушение требований по безопасному производству погрузочно-разгрузочных работ',
    'Нарушение требований по безопасному производству при эксплуатации машин и механизмов',
    'Нарушение требований к содержанию зданий, территорий, помещений',
    'Нарушение требований по безопасной эксплуатации оборудования',
    'Нарушение требований по оформлению документации',
    'Нарушение требований пожарной безопасности',
    'Нарушение требований безопасности дорожного движения',
    'Неприменение работниками СИЗ',
    'Прочие несоответствия',
  ];

  function collectFromCatalog(catalog) {
    const titles = new Set();
    const formulas = new Set();

    const scan = (akts) => {
      (akts || []).forEach((akt) => {
        (akt.violations || []).forEach((v) => {
          if (v.title?.trim()) titles.add(v.title.trim());
          if (v.formulaFromRules?.trim()) formulas.add(v.formulaFromRules.trim());
        });
      });
    };

    scan(catalog?.akts);
    scan(catalog?.trash);

    return {
      titles: [...titles].sort((a, b) => a.localeCompare(b, 'ru')),
      formulas: [...formulas].sort((a, b) => a.localeCompare(b, 'ru')),
    };
  }

  /** Места нарушений из всех актов + объекты справочника (для подсказок). */
  function collectMestaFromCatalog(catalog) {
    const counts = new Map();
    const add = (value) => {
      const s = String(value || '').trim();
      if (!s) return;
      counts.set(s, (counts.get(s) || 0) + 1);
    };

    const scanAkts = (akts) => {
      (akts || []).forEach((akt) => {
        (akt.violations || []).forEach((v) => add(v.mesto));
        (akt.objectsCheck || []).forEach((o) => {
          add(o.title);
          add(o.subTitle);
        });
      });
    };

    scanAkts(catalog?.akts);
    scanAkts(catalog?.trash);
    (catalog?.objects || []).forEach((o) => {
      add(o.title);
      add(o.subTitle);
    });

    return [...counts.entries()]
      .sort((a, b) => b[1] - a[1] || a[0].localeCompare(b[0], 'ru'))
      .map(([m]) => m);
  }

  return {
    VIOLATION_TYPES,
    MAPPING_SEED_TYPES,
    collectFromCatalog,
    collectMestaFromCatalog,
  };
})();
